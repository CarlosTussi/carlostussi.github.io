/* Set the width of the side navigation to 250px */
function openNav() {
    document.getElementById("main-menu-js").style.width = "250px";
    document.getElementById("bar-icon").style.display = "none";

    
}
  
  /* Set the width of the side navigation to 0 */
  function closeNav() {
    document.getElementById("main-menu-js").style.width = "0";
    document.getElementById("bar-icon").style.display = "block";

  }